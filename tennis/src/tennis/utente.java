package tennis;

public class utente {

	private int id;
	private String nome;
	private String cognome;
	private int eta;
	private char sesso;
	private String email;
	private String numero;
	private String password;
	
	public int getId() {
		return id;
	}
	
	public String getPassword() {
		return password;
	}

	public String getNumero() {
		return numero;
	}

	public String getEmail() {
		return email;
	}

	public char getSesso() {
		return sesso;
	}

	public int getEta() {
		return eta;
	}

	public String getCognome() {
		return cognome;
	}

	public String getNome() {
		return nome;
	}

}
